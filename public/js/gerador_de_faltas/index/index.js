// Criar hover na opção 1:
document.getElementById("option-1").addEventListener("mouseover", function () {
    document.getElementById("option-1-icon").classList.add("text-primary");
    document.getElementById("option-1-text").classList.add("text-primary");
});

document.getElementById("option-1").addEventListener("mouseout", function () {
    document.getElementById("option-1-icon").classList.remove("text-primary");
    document.getElementById("option-1-text").classList.remove("text-primary");
});

